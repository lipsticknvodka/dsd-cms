                <h1 class="col-xs-12">Our History</h1>
                <div class="col-sm-6">
                    <p>DSD Trucking was formally incorporated on March 3, 1986 in the state of California. The initial business model was primarily modeled on providing services for the ever-increasing demands of a wide variety of transportation services required to support the highly-specialized garment industry, located in the <a href="https://en.wikipedia.org/wiki/Los_Angeles_Fashion_District" target="_blank">Fashion District</a> in downtown Los Angeles, where it continues to thrive today. As the need for additional <a href="trucking">trucking services</a> grew, so did DSD. It expanded its presence in the <a href="trucking">pick-up and delivery services</a> throughout the <a href="https://en.wikipedia.org/wiki/Greater_Los_Angeles_Area" target="_blank">greater Los Angeles areas</a>.</p>

                    <p>In November of 1996, recognizing the need for an additional services near the LAX airport, DSD further expanded its presence into the highly-regulated, US Customs Bonded Warehousing Industry. This new venture, created what is today called DSD <a href="ocean-freight">CFS</a> (Container Freight Station). With this expansion, DSD entered into the ever-expanding community of the global trade industry, and the growing air freight industry. This expansion allowed DSD the opportunity to maximize its presence in the import traffic that was experiencing astronomical growth between the United Sates and the Far East. As the customer base grew, it became more diversified, as did the freight from around the world. It wasn’t long before, DSD was handling freight from all around the globe.</p>
                </div>
                <div class="col-sm-6">
                    <p>By the year 2000, DSD <a href="ocean-freight">CFS</a> (Container Freight Station) became the largest break-bulk operation within the LAX Airport area, handling approximately 100 million pounds (50 Million Kg) of <a href="air-freight">air freight</a> annually.
                        With the continued expansion of all import/export traffic, DSD also extended into <a href="ocean-freight">ocean freight</a>. Thereby, creating a natural expansion for DSD, to add an intermodal division in which to continue to support it’s ever-increasing customer base. </p>

                    <p>In August of 2010, a congressional amendment was passed by the United States Congress, known as the <strong><em>911 Act</em></strong>, which <strong><em>requires that all cargo being shipped on a passenger aircraft, be screened for explosives of any kind</em></strong>. DSD’s dedication to providing its customers with a full suite of necessary global services, became one of first container freight stations to offer a fully-staffed and functional CCSF (Certified Cargo Screening Facility), offering a full array of screening services, from an already built pallet to large oversized crates. DSD’s CCSF can handle all your needs. DSD provides <strong><em>global reach with a local touch</em></strong>.</p>
